<?php


/**
 * @param $data
 */
function dd($data)
{
    echo '<pre>';
    die(var_dump($data));
    echo '</pre>';
}


/**
 * @param $arr
 * @param bool $die
 */
function debug($arr, $die = false)
{
    echo '<pre>';
    print_r($arr);
    echo '</pre>';
    if($die) die;
}
















































































































































































































































































































































































































































































































































































































































